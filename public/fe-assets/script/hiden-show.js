

function toggleShow(){
    const bShow = document.getElementById("btnShow"),
          bHide = document.getElementById("btnHide");
          

    bShow.style.display = "none";
    bHide.style.display = "block";
};

function toggleHiden(){
    const bShow = document.getElementById("btnShow"),
          bHide = document.getElementById("btnHide");
          

    bShow.style.display = "block";
    bHide.style.display = "none";
};